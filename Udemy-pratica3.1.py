a = int(input('Digite um número inteiro: '))
b = int(input('Digite outro número inteiro: '))


soma = a + b
print(f' O resultado da soma de {a} com {b} é igual a {soma}')

subtração = a - b
print(f' O resultado da subtração de {a} e {b} é igual a {subtração}')

multiplicação = a * b
print(f' O resultado da multiplicação entre {a} e {b} é igual a {multiplicação}')

divisão = a / b
print(f'O resultado da divisão entre {a} e {b} é igual a {divisão}')

